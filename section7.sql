/* FILTRANDO VALORES NULOS */
SELECT nome, sexo, endereco
FROM cliente
WHERE email IS NULL; -- NÃO SE USA IGUALDADE ANTES DA PALAVRA NULL (email = NULL)

SELECT nome, sexo, endereco
FROM cliente
WHERE email IS NOT NULL;

