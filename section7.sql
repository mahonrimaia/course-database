/* WHERE - VAI SER O SEU MELHOR AMIGO DA VIDA PRA VIDA INTEIRA */

UPDATE cliente
SET email = 'JOAO@IG.COM.BR'
WHERE nome = 'JOAO';

/* A CLÁUSULA DELETE */
/* NOTA: ANTES DE EXECUTAR UM DELETE, EXECUTE UM SELECT PARA CONFERIR OS DADOS QUE SERÃO DELETADOS
NUNCA SE ESQUEÇA DE USAR O WHERE PARA FILTRAR OS REGISTROS QUE DEVEM SER DELETADOS
FAÇA UM COUNT PARA TER MAIS CERTEZA AINDA DE QUE A QUANTIDADE DE REGISTROS DELETADOS ESTÁ CORRETA */

SELECT *
FROM cliente
WHERE nome = 'ANA';

SELECT COUNT(*)
FROM cliente
WHERE nome = 'ANA';

DELETE
FROM cliente
WHERE nome = 'ANA';

